// Tambah (+), Kurang(-), Kali(*), Bagi(/)
// Modulus --> %
// Pangkat --> **
// Increment --> ++
// Decrement --> --

let x = 10;
console.log(x + 1);
console.log(x - 2);
console.log(x * 3);
console.log(x / 4);

console.log(x % 5);
console.log(x ** 2);

console.log(++x);
console.log(x++);
console.log(x);

let y = 10;
console.log(y--);
console.log(--y);
console.log(y);

